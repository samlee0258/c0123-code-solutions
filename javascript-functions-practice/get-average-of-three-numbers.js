/* exported getAverageOfThreeNumbers */
/*
  - create a function named getAverageOfThreeNumbers with three parameters "x", "y", and "z"
  - add x, y, and z together first and divide the result of that expression with 3 and assign
  the result of that expression to a variable
  - return the value of the variable
*/

function getAverageOfThreeNumbers(x, y, z) {
  var averageOfThreeNumbers = (x + y + z) / 3;
  return averageOfThreeNumbers;
}
