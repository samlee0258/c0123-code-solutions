/*
  - create a function named getAverageOfTwoNumbers with two parameters "x" and "y"
  - add x and y first and divide the result of that expression with 2 and assign it
  to a variable
  - return value of variable
*/

function getAverageOfTwoNumbers(x, y) {
  var averageOfTwoNumbers = (x + y) / 2;
  return averageOfTwoNumbers;
}
