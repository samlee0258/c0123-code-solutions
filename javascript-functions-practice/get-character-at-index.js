/* exported getCharacterAtIndex */
/*
  - create a function named getCharacterAtIndex with two parameters "string" and "index"
  - using the charAt method with the index parameter as the argument obtain the
  character at a specific index in the string and assign it to a variable
  - return the value of the variable
*/

function getCharacterAtIndex(string, index) {
  var characterAtIndex = string.charAt(index);
  return characterAtIndex;
}
