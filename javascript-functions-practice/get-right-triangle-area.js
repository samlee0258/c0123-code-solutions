/*
  - create a function named getRightTriangleArea with two parameters "base" and "height"
  - multiply the base and height then divide the result of that expression with 2 and
  assign it to a variable
  - return the value of the variable
*/

function getRightTriangleArea(base, height) {
  var area = (base * height) / 2;
  return area;
}
