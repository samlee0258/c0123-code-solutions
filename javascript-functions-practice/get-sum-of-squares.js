/*
  - create a funciton named getSumOfSquares with two parameters "x" and "y"
  - multiply x to x and y to in seperate parenthesis. With the result of those expressions
  add them together. Assign the result of the expression to a variable
  - return the value of the variable
*/

function getSumOfSquares(x, y) {
  var sum = (x * x) + (y * y);
  return sum;
}
