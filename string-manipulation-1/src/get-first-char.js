/* exported getFirstChar */
/*
  - define a function named getFirstChar with one parameter "string"
  - using the charAt method get the first character of the string and assign to
    a variable named firstChar
    - default charAt() with no argument will automatically start at index 0
      giving you the first character
  -return output
*/

function getFirstChar(string) {
  var firstChar = string.charAt();
  return firstChar;
}
